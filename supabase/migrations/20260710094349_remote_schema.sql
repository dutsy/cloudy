CREATE OR REPLACE FUNCTION public.add_to_table_ticket(p_table_number text, p_items jsonb, p_total_amount numeric)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
declare
  v_order_id uuid;
begin
  -- 1. Find an existing unpaid order for this table
  select id into v_order_id
  from orders
  where table_number = p_table_number
  and is_paid = false
  limit 1;

  -- 2. If no order found, create one
  if v_order_id is null then
    insert into orders (table_number, total_amount, is_paid, status)
    values (p_table_number, p_total_amount, false, 'pending')
    returning id into v_order_id;
  else
    -- 3. If order exists, update total
    update orders
    set total_amount = total_amount + p_total_amount,
        status = 'pending' -- Keep the order active in kitchen
    where id = v_order_id;
  end if;

  -- 4. Insert new items linked to the (new or existing) order
  insert into order_items (order_id, product_id, quantity, price_at_purchase, note)
  select 
    v_order_id, 
    (item->>'product_id')::uuid, 
    (item->>'quantity')::int, 
    (item->>'price_at_purchase')::numeric,
    item->>'note'
  from jsonb_array_elements(p_items) as item;
  
end;
$function$
;

