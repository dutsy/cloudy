 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
BEGIN
  INSERT INTO public.profiles (id, email, role)
  VALUES (new.id, new.email, 'client'); -- Defaults everyone to 'client'
  RETURN new;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.set_daily_order_number()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$DECLARE
  next_num INTEGER;
BEGIN
  -- Get the max number for today, or start at 1
  SELECT COALESCE(MAX(daily_order_number), 999) + 1 
  INTO next_num
  FROM public.orders
  WHERE DATE(created_at) = CURRENT_DATE;
  
  NEW.daily_order_number := next_num;
  RETURN NEW;
END;$function$
;

